//栈和队列的应用

//单调栈(题型单一，不会常考，了解即可) 
//常见模型：找出每个数左边离它最近的比它大/小的数

int tt = 0;
for (int i = 1; i <= n; i++){
	while(tt && check(q[tt], i)) tt--; //当栈不为空，而且检查单调性成立的话, 就弹出栈顶元素 
	stk[++tt] = i; //否则入栈 
} 

//单调队列
//常见模型：找出滑动窗口中的最大值和最小值
int hh = 0, tt = -1;
for (int i = 0; i < n; i++){
	while(hh <= tt && check_out(q[hh])) hh++; //判断队头是否滑出窗口
	while(hh <= tt && check(q[tt], i)) tt--;
	q[++tt] = i; 
} 

//注：单调栈和单调队列都是想办法维持一个单调性 




